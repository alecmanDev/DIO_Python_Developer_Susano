# São operadores para comparar dois valores

# Igualdade
saldo = 450
saque = 200
print(saldo == saque)

# Diferença
saldo = 450
saque = 200
print(saldo != saque)

# Maior que / maior ou igual
saldo = 450
saque = 200
print(saldo > saque)
print(saldo >= saque)

# Menor que / menor ou igual
saldo = 450
saque = 200
print(saldo < saque)
print(saldo <= saque)