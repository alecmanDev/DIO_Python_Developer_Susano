# São operadores utilizados para definir o valor inicial ou sobrescrever um valor de uma váriavel

# Atribução Simples
saldo = 500
print(saldo)

# Atribuição com adição
saldo = 500
saldo += 200
print(saldo)

# Atribuição com subtração
saldo = 500
saldo -= 200
print(saldo)

# Atribuição com multiplicação
saldo = 500
saldo *= 2
print(saldo)

# Atribuição com divisão
saldo = 500
saldo /= 5
print(saldo)

# Atribuição com módulo
saldo = 500
saldo %= 480
print(saldo)

# Atribuição com exponenciação
saldo = 50
saldo **= 2
print(saldo)