# O que é encapsulamento?

'''
🔐 Encapsulamento em POO
Encapsulamento é um dos princípios fundamentais da Programação Orientada a Objetos (POO). Ele consiste em restringir o acesso direto a certos atributos e métodos de uma classe, permitindo que a manipulação dos dados aconteça apenas por meio de métodos específicos.

🎯 Por que usar encapsulamento?
✅ Proteção de dados – evita modificações acidentais ou mal-intencionadas.
✅ Melhor organização do código – separa a lógica interna da classe de sua interface externa.
✅ Facilidade de manutenção – mudanças internas não afetam o código externo que usa a classe.
'''